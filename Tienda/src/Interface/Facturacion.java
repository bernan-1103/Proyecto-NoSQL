    private void jBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBuscarActionPerformed
        if (!(txtCodProducto.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Debe indicar el codigo del producto para realizar la busqueda");

        } else if (!P.existe(Integer.parseInt(txtCodProducto.getText()))) {
            JOptionPane.showMessageDialog(null, "Producto no Existe");
        } else {
            String[] parts;
            parts = P.buscar(Integer.parseInt(txtCodProducto.getText()));
            txtProducto.setText(parts[1]);
            jAgregarP.setEnabled(true);
           
        }       
        
    }//GEN-LAST:event_jBuscarActionPerformed

