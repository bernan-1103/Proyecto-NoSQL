    private Object[][] obtenerTabla() {
        Object Detalles[][] = new Object[modelo.getRowCount()][modelo.getColumnCount()];
        for (int i = 0; i < modelo.getRowCount(); i++) {
            for (int j = 0; j < modelo.getColumnCount(); j++) {
                Detalles[i][j] = modelo.getValueAt(i, j);
            }
        }
        return Detalles;
    }
    
    private void calcularTotal(){
        int Total = 0;
            for (int i = 0; i < modelo.getRowCount(); i++) {

                int var3 = Integer.parseInt(String.valueOf(modelo.getValueAt(i, 5)));
                Total = Total + var3;
            }
            txtVenta.setText(Integer.toString(Total)); 
            calcularDescuentoIV();
    }
    
    private void calcularDescuentoIV(){        
            double Descuento=0.00;
            int venta = Integer.parseInt(txtVenta.getText());
            
            if (txtDescuento.getText().equalsIgnoreCase("")) {
                Descuento = venta;
            } else if(Integer.parseInt(txtDescuento.getText())==0){               
                Descuento = venta;
            }else{                               
                Descuento = venta-((venta*Integer.parseInt(txtDescuento.getText()))/100);              
            }
            txtSubTotal.setText(Double.toString(Descuento));
            DecimalFormat decimales = new DecimalFormat("0.00");
            double IV = (Double.parseDouble(txtSubTotal.getText()) / 13);
            txtIVA.setText(decimales.format(IV));

            txtTOTAL.setText(decimales.format(IV + Descuento));
    }
    
