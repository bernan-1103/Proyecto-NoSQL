    private void jGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jGenerarActionPerformed
        if (completo()) {
            JOptionPane.showMessageDialog(null, "No puede dejar espacios en blanco en el formulario", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else if(!E.existe(Integer.parseInt(txtCodE.getText()))){
            JOptionPane.showMessageDialog(null, "El Empleado no Existe, Corrija Cod Empleado");
        }else {
            Object tabla[][] = obtenerTabla();
            String codProducto = "";
            String cantidad = "";
            String nombre = "";
            String precio = "";
            String total = "";
            for (int i = 0; i < modelo.getRowCount(); i++) {
                if (i == 0) {
                    codProducto = codProducto + tabla[i][0];
                    cantidad = cantidad + tabla[i][3];
                    nombre = nombre + tabla[i][1];
                    precio = precio + tabla[i][4];
                    total = total + tabla[i][5];
                } else {
                    codProducto = codProducto + "_" + tabla[i][0];
                    cantidad = cantidad + "_" + tabla[i][3];
                    nombre = nombre + "_" + tabla[i][1];
                    precio = precio + "_" + tabla[i][4];
                    total = total + "_" + tabla[i][5];
                }
            }

            F.agregar((F.NumFac()+1) + ";" + txtcodCliente.getText() + ";"
                    + txtCodE.getText() + ";" + F.fecha() + ";" + codProducto 
                    + ";"+ nombre + ";" + cantidad + ";" + precio + ";" + total
                    + ";" + txtVenta.getText() + ";" + txtDescuento.getText() 
                    + ";"+ txtSubTotal.getText() + ";" + txtIVA.getText()
                    + ";" +  txtTOTAL.getText());
            limpiarCampos();
            jGenerar.setEnabled(false);
                    
        }
        
    }//GEN-LAST:event_jGenerarActionPerformed

    private void jAgregarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAgregarPActionPerformed
          if (txtCodProducto.getText() == "" || txtProducto.getText() == "") {
            JOptionPane.showMessageDialog(null, "No puede dejar espacios en blanco en el formulario", "ERROR", JOptionPane.ERROR_MESSAGE);

        } else {
            String[] parts;
            parts=F.cargarModelo(Integer.parseInt(txtCodProducto.getText())
                    ,Integer.parseInt(txtCantP.getText())).split(";");
            
            modelo.addRow(new Object[]{parts[0],parts[1],parts[2],
                parts[3],parts[4],parts[5]});
            
              calcularTotal();
              
              txtCodProducto.setText("");
              txtProducto.setText("");
              jAgregarP.setEnabled(false);

        }
    }//GEN-LAST:event_jAgregarPActionPerformed

