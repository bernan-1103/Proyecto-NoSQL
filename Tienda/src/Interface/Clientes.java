    private void jAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAgregarActionPerformed
        model.cliente c = cliente.getCliente();

        c.setIdCliente(Integer.parseInt(txtCod.getText()));
        c.setNombre(txtNombre.getText());
        c.setPrimerApellido("ramirez");
        c.setSegundoApellido("G");
        c.setTelefono(Integer.parseInt(txtTelefono.getText()));
        c.setCorreo(txtCorreo.getText());

        Codigo.ClientesManager CM = ClientesManager.getClienteMC();
        CM.agregar();

        cargarTabla();

        limpiarCampos();

    }//GEN-LAST:event_jAgregarActionPerformed

    private void jEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jEliminarActionPerformed
        if (ClientesManager.getClienteMC().eliminarCliente(txtCod.getText())) {
            limpiarCampos();
        cargarTabla();
        JOptionPane.showMessageDialog(null, "El cliente ha sido Eliminado");
        }
  
        
    }//GEN-LAST:event_jEliminarActionPerformed

    private void jBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBuscarActionPerformed
        cliente c = ClientesManager.getClienteMC().buscar(Integer.parseInt(txtCod.getText()));
        if (c != null) {
            limpiarCampos();
            txtCod.setText(Integer.toString(c.getIdCliente()));
            txtNombre.setText(c.getNombre());
            txtTelefono.setText(Integer.toString(c.getTelefono()));
            txtCorreo.setText(c.getCorreo());
        }

    }//GEN-LAST:event_jBuscarActionPerformed

