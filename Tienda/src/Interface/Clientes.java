    public void limpiarCampos() {
        txtCorreo.setText("");
        txtTelefono.setText("");
        txtNombre.setText("");
        txtCod.setText("");
    }

    public void cargarTabla() {
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            Codigo.ClientesManager c = ClientesManager.getClienteMC();
            modelo.setColumnIdentifiers(new Object[]{"Cod", "Nombre", "Telefono", "Correo Electronico"});

            jTable1.setModel(c.cargarModelo(modelo));
        } catch (JAXBException | IOException ex) {
            Logger.getLogger(Clientes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private boolean completo() {
        return txtCod.getText().equals("") || txtCorreo.getText().equals("")
                || txtNombre.getText().equals("") || txtTelefono.getText().equals("");
    }

