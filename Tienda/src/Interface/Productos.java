    public void limpiarCampos(){        
                     txtCod.setText("");
                     txtNombre.setText("");
                     txtDescripcion.setText("");
                     txtCantidad.setText("");                      
                     txtPrecioC.setText(""); 
                     txtTalla.setText("");
                     txtPrecioV.setText("");
    }
    
    public void cargarTabla(){
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.setColumnIdentifiers(new Object[]{"Cod","Nombre","Descripcion",
            "Talla","Cantidad","Precio Compra","Precio Venta"});
        
        jTable1.setModel(p.cargarModelo(modelo));
    }
    
    private boolean completo(){
        return txtCod.getText().equals("") || txtDescripcion.getText().equals("")
                || txtNombre.getText().equals("") || txtTalla.getText().equals("")
                || txtCantidad.getText().equals("") || txtPrecioC.getText().equals("")
                || txtPrecioV.getText().equals("");
    }
       
    private void jAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAgregarActionPerformed
        if (completo()) {
            JOptionPane.showMessageDialog(null, "No puede dejar espacios en blanco en el formulario", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else if (p.existe(Integer.parseInt(txtCod.getText()))) {
            JOptionPane.showMessageDialog(null, "El Producto ya Existe");

        } else {
            p.agregar(txtCod.getText() + ";" + txtNombre.getText() + ";"
                    + txtDescripcion.getText() + ";" + txtTalla.getText()
                    + ";" + txtCantidad.getText() + ";" + txtPrecioC.getText() + ";" + txtPrecioV.getText());
            cargarTabla();
            limpiarCampos();
        }
    }//GEN-LAST:event_jAgregarActionPerformed

    private void jBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBuscarActionPerformed
             String[] busqueda=p.buscar(Integer.parseInt(txtCod.getText()));
        if (busqueda!=null) {
            limpiarCampos();
            txtCod.setText(busqueda[0]);
            txtNombre.setText(busqueda[1]);
            txtDescripcion.setText(busqueda[2]);
            txtTalla.setText(busqueda[3]);
            txtCantidad.setText(busqueda[4]);
            txtPrecioC.setText(busqueda[5]);
            txtPrecioV.setText(busqueda[6]);
        }else{
           JOptionPane.showMessageDialog(null,"Producto no existe","No encontrado", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jBuscarActionPerformed

    private void jEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jEliminarActionPerformed
        p.eliminar(Integer.parseInt(txtCod.getText()));
         limpiarCampos(); 
         cargarTabla();
    }//GEN-LAST:event_jEliminarActionPerformed

    private void jEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jEditarActionPerformed
        if (completo()) {
            JOptionPane.showMessageDialog(null, "No puede dejar espacios en blanco en el formulario", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            p.editar(txtCod.getText() + ";" + txtNombre.getText() + ";"
                    + txtDescripcion.getText() + ";" + txtTalla.getText()
                    + ";" + txtCantidad.getText() + ";" + txtPrecioC.getText() + ";" + txtPrecioV.getText());
        }
        limpiarCampos();
        cargarTabla();
    }//GEN-LAST:event_jEditarActionPerformed

